/**
 * This class is a pojo to store cell state.
 */
package com.wiki.poc.gol.bean;

/**
 * @author wiki
 */
public class Cell
{
    private char state;

    /**
     * @return the state
     */
    public char getState()
    {
        return state;
    }

    /**
     * @param state
     *            the state to set
     */
    public void setState( char state )
    {
        this.state = state;
    }

}
